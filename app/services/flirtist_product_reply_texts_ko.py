def ko_reply_texts(style: str, context: ReplyContext, focus: str | None) -> list[str]:
    topic = focus_or_topic(context.topic, focus)
    match context.scenario:
        case "celebration":
            table = _ko_celebration_table(topic)
        case "fatigue":
            table = _ko_fatigue_table(topic)
        case "reaction":
            table = _ko_reaction_table(topic)
        case "availability":
            table = ko_availability_table(topic)
        case "affection":
            table = _ko_affection_table(topic)
        case "plans":
            table = ko_plans_table(topic)
        case "generic":
            table = _ko_generic_table(topic)
        case unreachable:
            assert_never(unreachable)
    return table.get(style, table["genuine"])


